package datastructure.TD;

/**
 * two dimension point
 */
public class _2DPoint {
	public double x;
	public double y;

	public _2DPoint() {
	}

	public _2DPoint(double x, double y) {
		this.x = x;
		this.y = y;
	}

	@Override
	public String toString() {
		return "Point [x=" + x + ", y=" + y + "]";
	}

}